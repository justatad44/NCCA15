function retrieveJSON(filedict,filename){
    var jsonFile = Titanium.Filesystem.getFile(Titanium.Filesystem.applicationDataDirectory,filename);
    if (jsonFile.exists()) {
        var jsonFile = Titanium.Filesystem.getFile(Titanium.Filesystem.applicationDataDirectory,filename);
        var data = jsonFile.read().text;
        var jsonObj = JSON.parse(data);
        //loop through filedict to populate date via jsonObj
        for (var index in filedict){
            if (jsonObj.hasOwnProperty(index)){
                filedict[index] = jsonObj[index];
            }
        }
    }
}

